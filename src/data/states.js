/**
 * Lista completa de estados/provincias por país
 * Cada estado/provincia tiene formato ISO: [ISO país]-[Código estado]
 * Incluye todos los países con subdivisiones administrativas oficiales
 */

// Función para obtener los estados/provincias de un país específico
export const getStatesByCountry = (countryCode) => {
  // Si no hay una lista específica, generamos una entrada genérica basada en el país
  if (!statesByCountry[countryCode]) {
    return [getGenericState(countryCode)];
  }
  return statesByCountry[countryCode];
};

// Para los países que no tienen estados/provincias definidos en esta lista,
// podemos retornar un estado genérico "Not Applicable"
export const getGenericState = (countryCode) => {
  return {
    name: "Not Applicable",
    code: `${countryCode}-NA`
  };
};

